package model.game.team;

/**
 * The type Player.
 */
public class Player {
    /**
     * The Name.
     */
    private final String name;

    /**
     * Instantiates a new Player.
     *
     * @param name the name
     */
    public Player(String name) {
        this.name = name;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return this.name;
    }
}
